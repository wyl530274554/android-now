package com.melon.unity.function.money;

public class Money {
    public int id;
    public String title;
    public int level;
    public int amount;
    public int ratio;

    public Money(int id, String title, int level, int amount, int ratio) {
        this.id = id;
        this.title = title;
        this.level = level;
        this.amount = amount;
        this.ratio = ratio;
    }
}
